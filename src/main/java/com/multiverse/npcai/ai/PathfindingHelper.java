package com.multiverse.npcai.ai;

import java.util.List;

/**
 * NPC의 경로 탐색을 도와주는 헬퍼 클래스
 */
public class PathfindingHelper {
    /**
     * 간단한 경로 계산 예시
     * 실제 게임 환경에서는 좌표/맵 정보에 맞는 복잡한 로직이 필요
     */
    public List<String> calculatePath(String start, String destination) {
        // 더미 구현: 실제로는 좌표 기반 알고리즘 사용
        return List.of(start, destination);
    }
}